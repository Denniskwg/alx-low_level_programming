#include "3-calc.h"

/**
 * op_add - adds two numbers
 * @a: first number
 * @b: second number
 * Return: sum of two numbers
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtracts two numbers
 * @a: first number
 * @b: first number
 * Return: difference between two numbers
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplies two numbers
 * @a: first number
 * @b: second number
 * Return: multiplication of two numbers
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divides a by b
 * @a: first number
 * @b: second number
 * Return: result of dividing a and b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - returns the modulous of a after dividing by b
 * @a: first number
 * @b: second number
 * Return: modulous of a
 */
int op_mod(int a, int b)
{
	return (a % b);
}
